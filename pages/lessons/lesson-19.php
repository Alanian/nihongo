<p>
    With the <em>masu</em>-form, you can use the denial-form and past-tense very easily without knowing how it works on
    regular forms. Replace <em>masu</em> by <em>masen</em> to express a denying statement (do not) and <em>mashita</em>
    to move an action in the past (did). You can also deny an action in the past with <em>masen deshita</em> (did not).
    Of course we will cover denials and past tenses in further lessons.
</p>
