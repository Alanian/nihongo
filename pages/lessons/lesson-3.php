<p>
    Original Japanese isn't written with roman letters, but with
    <em>Kana<meaning>the japanese writing systems Hiragana and Katakana</meaning></em>
    and
    <em>Kanji<meaning>fancy hieroglyph-like pictures</meaning></em>.
    Anything you can say in Japanese you can write in
    <em>Kana<meaning>the japanese writing systems Hiragana and Katakana</meaning></em>,
    hence
    <em>Kanji<meaning>fancy hieroglyph-like pictures</meaning></em>
    can be written in
    <em>Kana<meaning>the japanese writing systems Hiragana and Katakana</meaning></em>
    too.
    <em>Kana<meaning>the japanese writing systems Hiragana and Katakana</meaning></em>
    consists of syllables, which pronunciation length is equal - called <strong>mora</strong>.
    Even though you don't know
    <em>Kana<meaning>the japanese writing systems Hiragana and Katakana</meaning></em>,
    you should think of syllables, instead of letters.
</p>
<p>
    So if you are spelling Japanese, you shouldn't spell it letter-wise, but mora-wise.
    The word <jap>はなび<meaning>fireworks</meaning></jap> is spelt "ha na bi", instead of "h a n a b i" and
    <jap>ちんたつげきとう<meaning>just a fictional word</meaning></jap> is spelt "chi n ta tsu ge ki to u".
    If you're asked, what the ending of <jap>まねく<meaning>to invite</meaning></jap>is, then it's not "u", but "ku".
</p>
<p>
    Please note that all mora's pronunciation lengths are equal. For instance, <em>n</em> is stressed like "nnh" and
    double consonants or double vocals have its combination written out. Still don't get it? See this
    <a href="hiragana.php">Hiragana-table</a>. Each character is exactly one mora.
</p>