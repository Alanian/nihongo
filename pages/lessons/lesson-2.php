<p>
    The first particle we will learn is
    <jap>の<meaning>possession particle</meaning></jap>
    with its possessive meaning. If there's a noun followed by
    <jap>の<meaning>possession particle</meaning></jap>,
    this noun is understood as the owner of something.
    This "something" can be an object or an attribute.
</p>
<p>
    <jap>まり<meaning>a girl's name</meaning></jap>
