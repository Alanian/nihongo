<h1>Lection <?= $_GET['lection']; ?> - <em>Ren'yōkei</em></h1>
<p>
    It's hard to describe, what a form stands for, because there's no clear or too complex definition for any of them. In
    further lections you will understand, how to use the <em>Ren'yōkei</em> (RYK) - The Continuative Form. For now just
    remember how to build it.
</p>
<p>
    Remember our verb groups from <a href="lections.php?lection=6">Lection 6</a>? Build them like following:
</p>
<ul>
    <li>
        <p>
            The (RYK) for <jap>する<meaning>to do; to make</meaning></jap> is <jap>し</jap>
        </p>
    </li>
    <li>
        <p>
            The (RYK) for <jap>くる<meaning>to come</meaning></jap> is <jap>き</jap>
        </p>
    </li>
    <li>
        <p>
            The (RYK) for lvl1 is quite simple, just the stem.<br>
            Example: <jap>たべる<meaning>to eat</meaning></jap> ➤ <jap>たべ</jap>
        </p>
    </li>
    <li>
        <p>
            The (RYK) for lvl5 is a little more complicated. The ending have to be replaced by its neighbour from the
            i-column.<br>
            Example: <jap>とぶ<meaning>to jump; to fly</meaning></jap> ➤ <jap>とび</jap>
        </p>
    </li>
</ul>
<p>
    "I got it, for lvl5 I just have to switch u and i."<br>
    Uhm... no. The ending is <em>bu</em>, so you replace it by <em>bi</em>.<br>
    "You're just splitting hairs!"<br>
    Maybe... but there's a reason. If I would follow this rule,
    <em>korosu<meaning>to kill; to murder</meaning></em> would became
    <em>korosi</em>, but there's no "si" in japanese
    language. The neighbour of <em>su</em> in the i-column is <em>shi</em>, so it's <em>koroshi</em>.
</p>

