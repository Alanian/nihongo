    Even though you don't know
    <em>Kana<meaning>the japanese writing systems Hiragana and Katakana</meaning></em>,
    you should think of syllables, instead of letters.
</p>
<p>
    So if you are spelling Japanese, you shouldn't spell it letterwise, but morawise.
