<h1>Lection <?= $_GET['lection'];?> - Spelling</h1>
<p>
    Original Japanese isn't written with roman letters, but with
    <em title="the japanese writing systems Hiragana and Katakana">Kana</em> and
    <em title="fancy hieroglyph-like pictures">Kanji</em>.
    Anything you can say in Japanese you can write in <em title="the japanese writing systems Hiragana and Katakana">Kana</em>, hence <em title="fancy hieroglyph-like pictures">Kanji</em> can be written in <em title="the japanese writing systems Hiragana and Katakana">Kana</em> too.
    <em title="the japanese writing systems Hiragana and Katakana">Kana</em> consists of syllables, which pronunciation length is equal - called <strong>mora</strong>. Even though you don't know <em title="the japanese writing systems Hiragana and Katakana">Kana</em>, you should think of syllables, instead of letters.
</p>
<p>
    So if you are spelling Japanese, you shouldn't spell it letterwise, but morawise.
    The word <jap title="fireworks">はなび</jap> is spelled ha-na-bi, instead of h-a-n-a-b-i.
    and <jap title="just a fictional word">ちんたつげきとう</jap> is spelled chi-n-ta-tsu-ge-ki-to-u.
</p>
<p>
    Note: <em title="the mora n">n</em>'s pronunciation length is equal to any other charcter (spell it nnh).
</p>
<p>
    If you're asked, what the ending of <jap title="to invite">まねく</jap> is, then it's not u, but ku.
</p>
