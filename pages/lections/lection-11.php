<h1>Lection <?= $_GET['lection']; ?> - Sentence Construction</h1>
<p>
    Sentences can get complicated in every language and Japanese isn't an exception. The basics are simple though. In
    English, you have the S-V-O (subject, object, verb) and in Japanese it's <strong>S-O-V</strong>. As mentioned before
    the predicate is always at the end. The subject and object can be omitted when clear, but never the predicate.
    Remember that a particle always sticks to the preceding word, you can take them as a solid block.
</p>
<p>
    To understand a sentence you should first considering the theme and omitted subjects/objects and then
    <strong>begin with the predicate</strong> and move <strong>from right to left</strong>, or else you will
    misunderstand its meaning.
</p>
<p>
    Sentence examples:
</p>
<ul>
    <li>
        <p>
            <jap>ほん<meaning>book</meaning></jap>
            <jap particle>を<meaning>object marker particle</meaning></jap>
            <jap>よむ<meaning>to read</meaning></jap>
            ➤ read a book<br>
            Starting with <jap>よむ<meaning>to read</meaning></jap>, you know that the main statement is "read".
            <jap>ほん<meaning>book</meaning></jap> <jap particle>を<meaning>object marker particle</meaning></jap>
            stick together (see <a href="lections.php?lection=1">Lection 1</a>) and indicate that something is to be done
            to the book, namely the predicate.
        </p>
    </li>
    <li>
        <p>
            <jap>こども<meaning>child</meaning></jap>
            <jap>の<meaning>possession particle</meaning></jap>
            <jap>おもちゃ<meaning>toy</meaning></jap>
            <jap>を<meaning>object marker particle</meaning></jap>
            <jap>とる<meaning>to take</meaning></jap>
            ➤ take child's toy / take a toy from a child<br>
            Separate the blocks from right to left, i.e. <jap>とる<meaning>to take</meaning></jap>,
            <jap>おもちゃ<meaning>toy</meaning></jap> <jap>を<meaning>object marker particle</meaning></jap>,
            and <jap>こども<meaning>child</meaning></jap> <jap>の<meaning>possession particle</meaning></jap>.
            Bind the blocks by asking for the next step: Take! Take what? A toy! Whom toy? Child's!
        </p>
    </li>
</ul>
<p>
    Don't hurry to translate by just seeing the sentence. If you aren't experienced, it's easier to translate it
    as "a child takes a toy" - which is wrong. I'm telling you this because of my personal failures.
</p>
