<h1>Lection <?= $_GET['lection']; ?> - Tenses</h1>
<p>
    Fortunately there are only two tenses in japanese, <strong>past and non-past</strong>. A non specified sentence has
    to be understood as planned, such as our examples in previous lections. It doesn't mean that other tenses can't be
    expressed, but that you haven't to learn new forms or concepts.
</p>
<p>
    Here's an example (ignore unknown grammar):<br>
    <jap>まり<meaning>a girl's name</meaning></jap>
    <jap>が<meaning>subject marker particle</meaning></jap>
    <jap>えいが<meaning>film; movie</meaning></jap>
    <jap particle>を<meaning>object marker particle</meaning></jap>
    <jap>みる<meaning>to see; to watch</meaning></jap>
    ➤ Mari watches a film.<br>
</p>
<p>
    It isn't specified, when she does it. It is a sure thing, that she <strong>hadn't</strong> done it and that she
    <strong>isn't</strong> doing it right now. We just know that she intends to do it sometime, maybe soon, maybe in her
    next life, or maybe it's just a habit of her.
</p>
